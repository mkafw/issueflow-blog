#!/usr/bin/env node
/**
 * Issueflow - 页面生成器
 * 
 * 将同步的 Issue 数据转换为 Next.js 页面
 * 
 * 用法:
 *   node scripts/generate-pages.js --input ./content/generated --output ./app
 */

const fs = require('fs');
const path = require('path');

// 解析命令行参数
function parseArgs() {
  const args = process.argv.slice(2);
  const options = {
    input: './content/generated',
    output: './app'
  };
  
  for (let i = 0; i < args.length; i++) {
    switch (args[i]) {
      case '--input':
        options.input = args[++i];
        break;
      case '--output':
        options.output = args[++i];
        break;
      case '--help':
      case '-h':
        console.log(`
Usage: node scripts/generate-pages.js [options]

Options:
  --input <path>   输入目录（同步后的 Issue 数据）
  --output <path>  输出目录（Next.js app 目录）
  --help, -h       显示帮助
`);
        process.exit(0);
    }
  }
  
  return options;
}

// 生成文章列表页面
function generateListPage(type, items) {
  const typeName = type.charAt(0).toUpperCase() + type.slice(1);
  const itemsJson = JSON.stringify(items, null, 2);
  
  let code = `// @generated by Issueflow - DO NOT EDIT
// Source: GitHub Issues

import { Metadata } from 'next';

export const metadata: Metadata = {
  title: '${typeName}',
  description: '${typeName} 列表 - Issueflow Blog',
};

interface Item {
  number: number;
  title: string;
  slug: string;
  url: string;
  status: string;
  labels: string[];
}

const items: Item[] = ${itemsJson};

export default function ${typeName}Page() {
  return (
    <div className="container mx-auto px-4 py-8">
      <header className="mb-8">
        <h1 className="text-4xl font-bold bg-gradient-to-r from-blue-400 to-purple-500 bg-clip-text text-transparent">
          ${typeName}
        </h1>
        <p className="text-gray-400 mt-2">共 {items.length} 条记录</p>
      </header>
      
      <div className="grid gap-6 md:grid-cols-2 lg:grid-cols-3">
        {items.map((item) => (
          <article
            key={item.number}
            className="group relative bg-gradient-to-br from-gray-800/50 to-gray-900/50 
                       backdrop-blur-sm rounded-xl p-6 border border-gray-700/50
                       hover:border-blue-500/50 hover:shadow-lg hover:shadow-blue-500/10
                       transition-all duration-300"
          >
            <a href="/${type}/${item.slug}" className="block">
              <h2 className="text-xl font-semibold text-gray-100 group-hover:text-blue-400 
                             transition-colors line-clamp-2">
                {item.title}
              </h2>
              
              <div className="flex flex-wrap gap-2 mt-3">
                {item.labels
                  .filter(l => l.startsWith('category/') || l.startsWith('status/'))
                  .map(label => {
                    const isPublished = label.includes('published');
                    const isInProgress = label.includes('in-progress');
                    const colorClass = isPublished 
                      ? 'text-green-400 bg-green-500/20 border-green-500/30'
                      : isInProgress 
                        ? 'text-yellow-400 bg-yellow-500/20 border-yellow-500/30'
                        : 'text-gray-400 bg-gray-500/20 border-gray-500/30';
                    return (
                      <span
                        key={label}
                        className="px-2 py-1 text-xs rounded-full border " + colorClass
                      >
                        {label.replace('category/', '').replace('status/', '')}
                      </span>
                    );
                  })}
              </div>
              
              <p className="text-sm text-gray-500 mt-3">
                GitHub Issue #{item.number}
              </p>
            </a>
          </article>
        ))}
      </div>
    </div>
  );
}
`;
  return code;
}

