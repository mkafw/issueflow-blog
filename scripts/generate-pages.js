// 主函数
async function main() {
  const options = parseArgs();
  
  const inputDir = path.resolve(options.input);
  const outputDir = path.resolve(options.output);
  
  if (!fs.existsSync(inputDir)) {
    console.error("错误: 输入目录不存在: " + inputDir);
    console.log('请先运行 sync-issues.js 同步 Issues');
    process.exit(1);
  }
  
  console.log("📄 正在生成页面...");
  console.log("   输入: " + inputDir);
  console.log("   输出: " + outputDir + "\n");
  
  // 读取索引文件
  const indexPath = path.join(inputDir, 'index.json');
  if (!fs.existsSync(indexPath)) {
    console.error("错误: 未找到 index.json，请先运行 sync-issues.js");
    process.exit(1);
  }
  
  const indexData = JSON.parse(fs.readFileSync(indexPath, 'utf-8'));
  
  // 生成首页
  const homePagePath = path.join(outputDir, 'page.tsx');
  fs.mkdirSync(path.dirname(homePagePath), { recursive: true });
  fs.writeFileSync(homePagePath, generateHomePage(indexData), 'utf-8');
  console.log("✓ 首页: page.tsx");
  
  // 为每种类型生成列表页和详情目录
  for (const [type, items] of Object.entries(indexData.by_type || {})) {
    // 列表页
    const listPagePath = path.join(outputDir, type, 'page.tsx');
    fs.mkdirSync(path.dirname(listPagePath), { recursive: true });
    fs.writeFileSync(listPagePath, generateListPage(type, items), 'utf-8');
    console.log("✓ " + type + " 列表: " + type + "/page.tsx");
    
    // 详情目录（每个 slug 一个目录）
    for (const item of items) {
      const detailPath = path.join(outputDir, type, item.slug, 'page.tsx');
      fs.mkdirSync(path.dirname(detailPath), { recursive: true });
      fs.writeFileSync(
        detailPath,
        generateDetailPage(type, item.slug, item.title),
        'utf-8'
      );
    }
  }
  
  console.log("\n✅ 页面生成完成！");
  console.log("   共生成 " + Object.keys(indexData.by_type || {}).length + " 个类型页面");
}

main().catch(err => {
  console.error("错误:", err.message);
  process.exit(1);
});
